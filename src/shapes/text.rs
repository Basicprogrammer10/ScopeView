use bitvec::prelude::*;

use super::point::Point;
use crate::render::{Line, Pos, Render};

pub struct Text {
    text: Vec<char>,
    start: Pos,
}

impl Text {
    pub fn new<T>(text: T, start: Pos) -> Self
    where
        T: AsRef<str>,
    {
        let chars = text.as_ref().chars().collect::<Vec<_>>();

        Self { text: chars, start }
    }
}

impl Render for Text {
    fn render(&self) -> Vec<Line> {
        let mut out = Vec::new();

        let mut x = self.start.x;
        let y = self.start.y;
        for i in self.text.iter() {
            let mut draw_char = FONT[*i as usize - 32].map(|x| x.view_bits::<Msb0>().to_owned());
            draw_char.reverse();

            for (yi, yv) in draw_char.iter().enumerate() {
                for (xi, xv) in yv.iter().enumerate() {
                    if !xv {
                        continue;
                    }

                    out.extend(
                        Point(Pos {
                            x: x + xi as f32 % 8.0,
                            y: y + yi as f32,
                        })
                        .render(),
                    )
                }
            }
            x += 9.0;
        }

        out
    }
}

